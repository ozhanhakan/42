#include <unistd.h>
#include <stddef.h>
#include "libft.h"

/**
 * Outputs the string ’s’ to the specified file descriptor.
 */
void	ft_putstr_fd(char *s, int fd)
{
	size_t	len;

	if (!s)
		return ;
	len = ft_strlen(s);
	write(fd, s, len);
}
