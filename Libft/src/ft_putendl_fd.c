#include "libft.h"
#include <unistd.h>

/**
 * @brief Outputs the string ’s’ to the specified file descriptor 
 * followed by a newline.
 * @param s: The string to output.
 * @param fd: The file descriptor on which to write.
 */
void	ft_putendl_fd(char *s, int fd)
{
	if (!s)
		return ;
	ft_putstr_fd(s, fd);
	ft_putchar_fd('\n', fd);
}
